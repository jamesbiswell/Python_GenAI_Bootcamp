# Mastering Python Package Management with pip

# 1. Installing Pip: python -m pip install --upgrade pip

# 2. Installing a Package: pip install package_name

# 3. Upgrading a Package: pip install -- upgrade package_name

# 4. Listing Installed Packages: pip list

# 5. Uninstalling a Package: pip uninstall package_name

# 6. Using a Requirements File:
#    - Create a requirements.txt file with package names and versions: pip freeze > requirements.txt
#    - Install packages from the file: pip install -r requirements.txt